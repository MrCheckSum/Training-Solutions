package exam03;

public class Boat {

    private String name;
    private int maxPassanger;

    public Boat(String name, int maxPassanger) {
        this.name = name;
        this.maxPassanger = maxPassanger;
    }

    public String getName() {
        return name;
    }

    public int getMaxPassengers() {
        return maxPassanger;
    }

}
